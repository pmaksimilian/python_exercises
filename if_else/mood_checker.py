mood = input("What mood are you in? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times!")
elif mood == "sad":
    print("Hey, cheer up!")
elif mood == "excited":
    print("And you just can't hide it!")
elif mood == "relaxed":
    print("Did you smoke something?")
else:
    print("Hm, this is new. So far I only learned about happy, nervous, sad, excited and relaxed.")